## Channel Attention Layer
class CALayer(nn.Module):
    def __init__(self, channel, reduction=16, bias=False):
        super(CALayer, self).__init__()
        # global average pooling: feature --> point
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        # feature channel downscale and upscale --> channel weight
        self.conv_du = nn.Sequential(
            nn.Conv2d(channel, channel // reduction, 1, padding=0, bias=bias),  # 1x1 kernel -> 3x3 kernel
            nn.ReLU(inplace=True),
            nn.Conv2d(channel // reduction, channel, 1, padding=0, bias=bias),
            nn.Sigmoid()
        )

    def forward(self, x):
        y = self.avg_pool(x)
        y = self.conv_du(y)
        return x * y





class SALayer(nn.Module):
    def __init__(self, channel, reduction=16, bias=False):
        super(SALayer, self).__init__()
        # global average pooling: feature --> point
        # self.avg_pool_sa = torch.mean(dim=1, keepdim=True)
        # feature channel downscale and upscale --> channel weight
        self.conv_du_sa = nn.Sequential(
            nn.Conv2d(1, 1, 3, padding=1, bias=bias),  # 1x1 kernel -> 3x3 kernel
            nn.ReLU(inplace=True),
            nn.Conv2d(1, 1, 3, padding=1, bias=bias),
            nn.Sigmoid()
        )

    def forward(self, x):
        y = torch.mean(x, dim=1, keepdim=True)
        # y = self.avg_pool_sa(x)
        y = self.conv_du_sa(y)
        return x * y



## Channel Attention Block (CAB)
class CAB(nn.Module):
    def __init__(self, n_feat, kernel_size, reduction, bias, act):
        super(CAB, self).__init__()
        modules_body = []
        modules_body.append(conv(n_feat, n_feat, kernel_size, bias=bias))
        modules_body.append(act)
        # modules_body.append(nn.InstanceNorm2d(n_feat))
        modules_body.append(conv(n_feat, n_feat, kernel_size, bias=bias))

        self.CA = CALayer(n_feat, reduction, bias=bias)
        self.body = nn.Sequential(*modules_body)

    def forward(self, x):
        res = self.body(x)
        res = self.CA(res)
        res += x
        return res



